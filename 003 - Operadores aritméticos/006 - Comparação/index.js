/*
    ==, ===, !=, !==, >, <, >=, <=
*/

console.log(1 == "1"); // Igualdade
console.log(1 === "1"); // Igualdade estrita
console.log(1 != "1"); // Diferenca
console.log(1 !== "1"); // Diferenca estrita

console.log("-------------");

console.log(1 > 2); // Maior
console.log(1 < 2); // Menor
console.log(1 >= 2); // Maior ou Igual
console.log(1 <= 2); // Menor ou Igual
